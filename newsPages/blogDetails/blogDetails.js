// Get index from URL
function getBlogIndexFromURL() {
    const params = new URLSearchParams(window.location.search);
    let index = parseInt(params.get("blog"));
    if (isNaN(index) || index < 0 || index >= blogs.length) {
        index = 0; // default
    }
    return index;
}

function showBlog(index) {
    const blog = blogs[index];
    document.querySelector(".post-title").textContent = blog.title;
    document.querySelector(".post-image").src = blog.image;
    document.querySelector(
        ".date"
    ).innerHTML = `<i class="fa fa-calendar"></i> ${blog.date}`;
    document.querySelector(
        ".views"
    ).innerHTML = `<i class="fa fa-eye"></i> ${blog.views} Views`;
    document.querySelector(
        ".comments"
    ).innerHTML = `<i class="fa fa-comment"></i> ${blog.comments} Comments`;
    document.querySelector(".post-tags").innerHTML = blog.tags
        .map((tag) => `<a href="#" class="tag">${tag}</a>`)
        .join("");
    document.querySelector(".post-content").innerHTML = blog.content;

    // Update Prev / Next links with actual page reload
    const prevIndex = (index - 1 + blogs.length) % blogs.length;
    const nextIndex = (index + 1) % blogs.length;
    document.querySelector(
        ".prev-post a"
    ).href = `blogDetails.html?blog=${prevIndex}`;
    document.querySelector(
        ".next-post a"
    ).href = `blogDetails.html?blog=${nextIndex}`;
}

// Initial render
const currentIndex = getBlogIndexFromURL();
showBlog(currentIndex);
